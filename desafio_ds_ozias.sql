-- LISTA DE CADASTROS COM TAGS
SELECT c.nome, c.estado, c.cidade, GROUP_CONCAT(t.titulo) AS tags
FROM cadastros c
LEFT JOIN cadastros_tags ct ON c.id = ct.cadastro_id
LEFT JOIN tags t ON ct.tag_id = t.id
GROUP BY c.id;

-- LISTA DAS RECEITAS LIQUIDAS
SELECT c.nome, c.documento, l.descricao, l.liquidacao, l.valor_liquidado
FROM lancamentos l
LEFT JOIN cadastros c ON l.cadastro_id = c.id
WHERE l.tipo = 'receber' AND l.status = 'liquidado'
AND l.liquidacao BETWEEN '2022-12-31' AND '2024-12-31';

-- LISTA DE TOTAL DE RECEITAS LIQUIDAS POR CATEGORIA MENSAL
SELECT DATE_FORMAT(l.liquidacao, '%Y-%m') AS mes, cat.titulo, SUM(l.valor_liquidado) AS total
FROM lancamentos l
LEFT JOIN categorias cat ON l.categoria_id = cat.id
WHERE l.tipo = 'receber' AND l.status = 'liquidado'
GROUP BY mes, cat.id;

-- LISTA DE DESPESAS EM ABERTO
SELECT c.nome, c.documento, l.descricao, l.vencimento, l.valor
FROM lancamentos l
LEFT JOIN cadastros c ON l.cadastro_id = c.id
WHERE l.tipo = 'pagar' AND l.status = 'aberto';

-- LISTA DE TOTAL DE DESPESAS LIQUIDAS POR CATEGORIA MENSAL
SELECT DATE_FORMAT(l.liquidacao, '%Y-%m') AS mes, cat.titulo, SUM(l.valor_liquidado) AS total
FROM lancamentos l
LEFT JOIN categorias cat ON l.categoria_id = cat.id
WHERE l.tipo = 'pagar' AND l.status = 'liquidado'
GROUP BY mes, cat.id;